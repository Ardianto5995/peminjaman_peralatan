    public void tampil_jenis() {
        try {
            Koneksi conn = new Koneksi();
            Statement stat = conn.buatkoneksi().createStatement();
            String sql = "Select * from alat_kerja group by jenis";
            ResultSet res = stat.executeQuery(sql);

            while (res.next()) {
                cbJenis.addItem(res.getString("jenis"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan.." + e);
        }
    }

    public void daftarData() {
        try {
            Koneksi con = new Koneksi();
            try (Statement stat = con.buatkoneksi().createStatement()) {
                ResultSet rs = stat.executeQuery(sqlcreate);
                tableModel.setRowCount(0);
                while (rs.next()) {
                    String id = rs.getString("id_alat_kerja"); //'id_alat_kerja' sesuai field di DB
                    String nama = rs.getString("nama_alat"); //'nama_alat' sesuai field di DB
                    String ket = rs.getString("jenis"); //'jenis' sesuai field di DB
                    String status = rs.getString("status"); //'status' sesuai field di DB

                    String[] data = {id, nama, ket, status};
                    tableModel.addRow(data);
                }
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(rootPane, "SQL Error");
        }
    }
