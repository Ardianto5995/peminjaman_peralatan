    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            int row = tabel.rowAtPoint(evt.getPoint());

            String id = tabel.getValueAt(row, 0).toString();
            String nama = tabel.getValueAt(row, 1).toString();
            String ket = tabel.getValueAt(row, 2).toString();
            String stat = tabel.getValueAt(row, 3).toString();

            id_alat_kerja.setText(String.valueOf(id));
            nama_alat.setText(String.valueOf(nama));
            cbJenis.setSelectedItem(String.valueOf(ket));
            status.setText(String.valueOf(stat));
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        try {
            
            String sql = "insert into alat_kerja " + "(id_alat_kerja,nama_alat,jenis,status) values(?,?,?,?)";
            Koneksi k = new Koneksi();  //membuat objek dengan nama k
            Connection con = k.buatkoneksi();
            try (PreparedStatement stat = con.prepareStatement(sql)) {
                try {
                    stat.setString(1, id_alat_kerja.getText());
                    stat.setString(2, nama_alat.getText());
                    stat.setString(3, (String) cbJenis.getSelectedItem());
                    stat.setString(4, status.getText());
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data sudah tersimpan");
                } catch (SQLException se) {
                    JOptionPane.showMessageDialog(null, "ID Alat Kerja ini sudah ada dalam database");
                }
            }
            daftarData();
        } catch (SQLException se) {
            System.out.println("Gagal Simpan...");
        }
        aktif();
        kosong();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
        try {
            String sql = "update alat_kerja set id_alat_kerja=?,nama_alat=?,jenis=?,status=? where id_alat_kerja='" + id_alat_kerja.getText() + "'";
            Koneksi k = new Koneksi();  //membuat objek dengan nama k
            Connection con = k.buatkoneksi();
            try (PreparedStatement stat = con.prepareStatement(sql)) {
                try {
                    stat.setString(1, id_alat_kerja.getText());
                    stat.setString(2, nama_alat.getText());
                    stat.setString(3, (String) cbJenis.getSelectedItem());
                    stat.setString(4, status.getText());
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data sudah di update");
                } catch (SQLException se) {
                    JOptionPane.showMessageDialog(null, "Update data gagal...");
                }
            }
            daftarData();
        } catch (SQLException se) {
            System.out.println("Gagal Simpan...");
        }
        aktif();
        kosong();
    }//GEN-LAST:event_bupdateActionPerformed

