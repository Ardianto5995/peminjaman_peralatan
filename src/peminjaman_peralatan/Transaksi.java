    public void daftarData() {
        try {
            Koneksi con = new Koneksi();
            try (Statement stat = con.buatkoneksi().createStatement()) {
                ResultSet rs = stat.executeQuery(sqlcreate);
                tableModel.setRowCount(0);
                while (rs.next()) {
                    String id = rs.getString("id_trx");
                    String nik = rs.getString("nik");
                    String nama = rs.getString("nama_karyawan");
                    String ket = rs.getString("nama_dept");
                    String tgl = rs.getString("tanggal");
                    String shift = rs.getString("shift");
                    String alat = rs.getString("nama_alat");
                    String status = rs.getString("trx");
                    String[] data = {id, nik, nama, ket, tgl, shift, alat, status};
                    tableModel.addRow(data);
                }
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(rootPane, "SQL Error" + sq);
        }
    }

