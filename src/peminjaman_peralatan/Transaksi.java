    private void insertTransaksiIn() {
        try {
            String sql = "insert into transaksi " + "(tanggal,id_alat_kerja,nik,shift,trx) values(?,?,?,?,?);";
            Koneksi k = new Koneksi();  //membuat objek dengan nama k
            Connection con = k.buatkoneksi();
            try (PreparedStatement stat = con.prepareStatement(sql)) {
                try {

                    stat.setString(1, txtTanggal.getText());
                    stat.setString(2, txtNoAlat.getText());
                    stat.setString(3, txtNik.getText());
                    stat.setString(4, (String) cbShift.getSelectedItem());
                    stat.setString(5, "peminjaman");

                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Alat " + txtNoAlat.getText() + "Dipinjam");

                } catch (SQLException se) {
                    JOptionPane.showMessageDialog(null, "Gagal " + se);
                }
            }
        } catch (SQLException se) {
            System.out.println("Gagal Simpan...");
        }

    }

