    private void tampil_id() {
        try {
            Koneksi conn = new Koneksi();
            Statement stat = conn.buatkoneksi().createStatement();
            String sql = "SELECT * FROM alat_kerja WHERE id_alat_kerja = '" + txtNoAlat.getText() + "'";
            ResultSet res = stat.executeQuery(sql);

            if (res.next()) {

                String is_no_alat = res.getString("id_alat_kerja");
                String is_nama_alat = res.getString("nama_alat");
                String status = res.getString("status");

                if (!status.equalsIgnoreCase("tersedia")) {
                    JOptionPane.showMessageDialog(null, "Alat dengan ID ini sudah dipinjam silahkan CHECKOUT Terlebih dahulu");
                    tampil_alat_by_id();
                    btnCheckIn.setEnabled(false);
                    btnCheckOut.setEnabled(true);
                } else {
                    bersih();
                    txtNoAlat.setText(is_no_alat);
                    txtNamaAlat.setText(is_nama_alat);
                    btnCheckIn.setEnabled(true);
                    btnCheckOut.setEnabled(false);
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Salah.." + e);
        }
        txtNamaAlat.setEnabled(false);
    }

    private void tampil_alat_by_id() {
        try {
            Koneksi conn = new Koneksi();
            Statement stat = conn.buatkoneksi().createStatement();
            String sql = "SELECT t.id_trx, t.id_alat_kerja, a.nama_alat, a.status, t.tanggal, t.nik, t.shift,k.nama_karyawan FROM alat_kerja a,transaksi t, karyawan k "
                    + "WHERE a.id_alat_kerja = t.id_alat_kerja AND t.nik = k.nik AND a.id_alat_kerja = '" + txtNoAlat.getText() + "' ORDER BY t.id_trx DESC LIMIT 1";
            ResultSet res = stat.executeQuery(sql);

            if (res.next()) {
                String is_nama_alat = res.getString("nama_alat");
                String is_nama = res.getString("nama_karyawan");
                String is_shift = res.getString("shift");
                String is_nik = res.getString("nik");
                String is_no_alat = res.getString("id_alat_kerja");

                String status = res.getString("status");

                if (!status.equalsIgnoreCase("tersedia")) {
                    txtNama.setText(is_nama);
                    txtNamaAlat.setText(is_nama_alat);
                    txtNik.setText(is_nik);
                    txtNoAlat.setText(is_no_alat);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Salah.." + e);
        }
        txtNamaAlat.setEnabled(false);
    }

