    public void tampil_kode_dept()
    {
    String sql = "SELECT * FROM departemen WHERE nama_dept = '"+nama_dept.getSelectedItem()+"'";
    try{
        
            Koneksi conn = new Koneksi();
        try (Statement stat = conn.buatkoneksi().createStatement()) {
            ResultSet res=stat.executeQuery(sql);
            if(res.next())
            {
                String isi_nim = res.getString("id_dept");
                id_dept.setText(isi_nim);
            }
        } 
        }
        catch (Exception e) {
            //JOptionPane.showMessageDialog(null,"Terjadi Kesalahan.."+e);
        }
        }
    
     public void daftarData() {
        try {
            Koneksi con = new Koneksi();
            try (Statement stat = con.buatkoneksi().createStatement()) {
                ResultSet rs = stat.executeQuery(sqlcreate);
                tableModel.setRowCount(0);
                while (rs.next()) {
                    String id = rs.getString("nik"); //'id_alat_kerja' sesuai field di DB
                    String nama = rs.getString("nama_karyawan"); //'nama_alat' sesuai field di DB
                    String ket = rs.getString("nama_dept"); //'jenis' sesuai field di DB
                   
                   
                    String[] data = {id, nama, ket};
                    tableModel.addRow(data);
                }
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(rootPane, "SQL Error");
        }
    }
    DefaultTableModel tableModel;
    String sqlcreate;
    
