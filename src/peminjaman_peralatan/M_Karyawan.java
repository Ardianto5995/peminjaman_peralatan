    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        try {
            String sql = "insert into karyawan " + "(nik,nama_karyawan,id_dept) values(?,?,?)";
            Koneksi k = new Koneksi();  //membuat objek dengan nama k
            Connection con = k.buatkoneksi();
            try (PreparedStatement stat = con.prepareStatement(sql)) {
                try {
                    stat.setString(1, nik.getText());           //mengisi textfield id
                    stat.setString(2, nama_karyawan.getText());         //mengisi textfield nama_konsumen
                    stat.setString(3, id_dept.getText());         //mengisi textfield nama_konsumen
                   
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data sudah tersimpan");
                   
                } catch (SQLException se) {
                    JOptionPane.showMessageDialog(null, "Kode Jurnal ini sudah ada dalam database");
                }
            }
        } catch (SQLException se) {
            System.out.println("Gagal Simpan...");
        }
         aktif();
        kosong();
    }//GEN-LAST:event_bsimpanActionPerformed

