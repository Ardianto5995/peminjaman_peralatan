    public void daftarData() {
        try {
            Koneksi con = new Koneksi();
            try (Statement stat = con.buatkoneksi().createStatement()) {
                ResultSet rs = stat.executeQuery(sqlcreate);
                tableModel.setRowCount(0);
                while (rs.next()) {
                    String id = rs.getString("no_kerusakan"); //'id_alat_kerja' sesuai field di DB
                    String nik = rs.getString("tanggal"); //'id_alat_kerja' sesuai field di DB
                    String nama = rs.getString("nama_alat"); //'nama_alat' sesuai field di DB
                    String ket = rs.getString("nama_kerusakan"); //'jenis' sesuai field di DB
                    String tgl = rs.getString("tgl_selesai_perbaikan"); //'jenis' sesuai field di DB
                    String status = rs.getString("status"); //'jenis' sesuai field di DB
                    String[] data = {id, nik, nama, ket, tgl, status};
                    tableModel.addRow(data);
                }
            }
        } catch (SQLException sq) {
            JOptionPane.showMessageDialog(rootPane, "SQL Error");
        }
    }
