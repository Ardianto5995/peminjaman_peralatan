    public void tampil_ID_Service() {
        try {
            Koneksi conn = new Koneksi();
            Statement stat = conn.buatkoneksi().createStatement();
            String sql = "Select MAX(right(no_kerusakan,3)) as no from kerusakan";
            ResultSet res = stat.executeQuery(sql);

            while (res.next()) {
                if (res.first() == false) {
                    no_kerusakan.setText("KR0001");

                } else {
                    res.last();
                    int auto_id = res.getInt(1) + 1;
                    String no = String.valueOf(auto_id);
                    int noLong = no.length();
                    //MENGATUR jumlah 0
                    for (int a = 0; a < 3 - noLong; a++) {
                        no = "0" + no;
                    }
                    no_kerusakan.setText("KR0" + no);

                }
            }
            res.close();
            stat.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "ERROR: \n" + e.toString(),
                    "Kesalahan", JOptionPane.WARNING_MESSAGE);

        }
    }

